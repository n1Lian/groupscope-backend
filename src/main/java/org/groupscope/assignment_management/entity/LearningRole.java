package org.groupscope.assignment_management.entity;

/**
 * This enumeration represents different roles that a learner can have.
 * It defines two constant values: STUDENT and HEADMAN.
 */

public enum LearningRole {
    // Represents the role of a regular student.
    STUDENT,

    EDITOR,
    // Represents the role of a headman who can monitor student's grades.
    HEADMAN
}
